package javaprogramming;

public class math_equation_in_java {
    public static void main(String[] args) {
        int x,y,z;
        x=6;y=1;z=2;

        int result;
        result = (x-y)/z;
        System.out.println(result);

        result = y*y - (4*x*z)/(2*x);
        System.out.println(result);

        result = y*y - z*z;
        System.out.println(result);

        result = x*y-z;
        System.out.println(result);





    }
}
