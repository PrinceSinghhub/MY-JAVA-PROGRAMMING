package javaprogramming;

public class do_while_loop {
    public static void main(String[] args){

        int n = 1;
        do{
            System.out.println("Hello");

        }while (n!=1);

        int a = 5;
        do{
            System.out.println("Hello ji");
            a--;
        }while (a!=1);

    }




}
